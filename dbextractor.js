const extractEvents = async (input) => {
    try {
        const $rs = fs.createReadStream(`./data/input/${input}`);
        const $input = $rs.pipe(zlib.createGunzip());
        const lineReader = readline.createInterface({
            input: $input,
            crlfDelay: Infinity,
        });
        let parsed = 0;
        console.log(`Search for events in ${input}`);
        for await (const line of lineReader) {
            parsed++;
            if (!EVENT_FILTER.test(line)) { continue; }
            const record = parseRecord(JSON.parse(line).Item);
            if (!EVENTS.has(record.id)) { continue; }
            events++;
            const dest = path.join(`./data/input/`, EVENTS.get(record.id), `/event-${record.sort}.json`);
            await fse.outputJSON(dest, record);
            if (events % 100 === 0) {
                console.log(`Found ${events} events thus far`);
            }
        }
        console.log(`Read ${parsed} lines out of ${input} looking for events`);
        await new Promise((resolve) => {
            lineReader.close();
            $input.close((e2) => {
                if (e2) { console.log(e2); }
                $rs.close((e3) => {
                    if (e3) { console.log(e3); }
                    console.log(`${input} closed`);
                    resolve();
                });
            });
        });
    } catch (e) {
        console.log(`Error parsing ${input} for events`, e);
    }
};

const extractResults = async (input) => {
    const $rs = fs.createReadStream(`./data/input/${input}`);
    const $input = $rs.pipe(zlib.createGunzip());
    const lineReader = readline.createInterface({
        input: $input,
        crlfDelay: Infinity,
    });

    for await (const line of lineReader) {
        lines++;
        if (!RESULT_FILTER.test(line)) { continue; }
        // record.Item.event?.M?.id?.S === "init"
        const record = parseRecord(JSON.parse(line).Item);
        const fields = [
            record.systemid ?? "nothing",
            record.pid ?? "nothing",
            record.seed ?? 0,
            record.testid ?? "",
            record.status ?? "",
            record.returnUrl ?? ""
        ];
        if (!record.extracthtml
            || isNaN(fields[2])
            || fields[2] < 1679288400000 // Mon Mar 20 2023 00:00:00 GMT-0500 (Central Daylight Time)
            // || fields[2] > 1680325200000 // Sat Apr 01 2023 00:00:00 GMT-0500 (Central Daylight Time)
            || fields[2] > 1680498000000 // Mon Apr 03 2023 00:00:00 GMT-0500 (Central Daylight Time)
            || fields[0] !== "ac-aatprodlon01-prod"
            || fields[4] === "preview"
        ) {
            continue;
        }
        records++;
        if (KNOWN_PIDS.has(fields[1])) {
            known++;
            found.add(fields[1]);
        }
        PIDS.add(record.pid);
        EVENTS.set(`EVENT-${record.systemid}-${record.pid}`, pidToPath(record.pid));
        isolated.write(`${JSON.stringify(record)}\n`);
        output.write(`${fields.join(",")}\n`);
        if (records && records % 100 === 0) {
            console.log(`Found ${records} records thus far`);
        }
    }
    console.log(`Parsed ${lines} lines as of ${input}`);
    await new Promise((resolve) => {
        lineReader.close();
        $input.close((e2) => {
            if (e2) { console.log(e2); }
            $rs.close((e3) => {
                if (e3) { console.log(e3); }
                console.log(`${input} closed`);
                console.log(`Found ${records} records thus far`);
                resolve();
            });
        });
    });
};

